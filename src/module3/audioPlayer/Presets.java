package module3.audioPlayer;

public class Presets {

    int bass;
    int midrange;
    int treble;
    int balance;
    int volume;

    public Presets(int bass, int midrange, int treble, int balance, int volume) {
        this.bass = bass;
        this.midrange = midrange;
        this.treble = treble;
        this.balance = balance;
        this.volume = volume;
    }
}
